<h3 class="h3">Sustaining green Gujarat</h3>
<p class="sust_txt">Gujarat has a 1600 kms long and varied coastline, a complex geomorphological evolution, a very wide range of climatic variability and uniqueness of flora and fauna. Vastly diverse ecosystems co-exist. Some of them are very fragile and already under great risk. Attempt to achieve faster economic growth are putting a progressively increasing pressure on environment. Gujarat is amongst the most industrialized states in India; and also leads in urbanization. Proactive and innovative efforts are required to optimize the use of natural resources. Rapid economic development has to be balanced with environmental concerns in public health, productivity, provision of public amenities and maintenance of bio-diversity.</p>
